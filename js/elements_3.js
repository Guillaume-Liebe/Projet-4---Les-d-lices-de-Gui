   
   /*Agrandissements images Entremet*/
   var photos_entremet_1_miniature = document.getElementById('photos_entremet_1')

   photos_entremet_1_miniature.addEventListener('click', function () {
   document.getElementById('photos_entremet_1_agrandi')
   photos_entremet_1_agrandi.style.visibility = 'visible';
   document.getElementById('arrierePlan')
   arrierePlan.style.visibility = 'visible';
   })

   photos_entremet_1_miniature.addEventListener('click', function () {
   document.getElementById('arrierePlan')
   photos_entremet_1_agrandi.style.visibility = 'visible';
   })

   arrierePlan.addEventListener('click', function () {
   document.getElementById('arrierePlan')
   arrierePlan.style.visibility = 'hidden';
   photos_entremet_1_agrandi.style.visibility = 'hidden';
   })


