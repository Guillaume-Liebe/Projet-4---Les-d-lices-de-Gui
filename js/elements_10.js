
/*Agrandissements images Mille Feuille*/
var photos_mille_feuille_1_miniature = document.getElementById('photos_mille_feuille_1')

photos_mille_feuille_1_miniature.addEventListener('click', function () {
document.getElementById('photos_mille_feuille_1_agrandi')
photos_mille_feuille_1_agrandi.style.visibility = 'visible';
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'visible';
})

photos_mille_feuille_1_miniature.addEventListener('click', function () {
document.getElementById('arrierePlan')
photos_mille_feuille_1_agrandi.style.visibility = 'visible';
})

arrierePlan.addEventListener('click', function () {
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'hidden';
photos_mille_feuille_1_agrandi.style.visibility = 'hidden';
})


