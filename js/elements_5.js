   
/*Agrandissements images Paris Brest*/
var photos_paris_brest_1_miniature = document.getElementById('photos_paris_brest_1')

photos_paris_brest_1_miniature.addEventListener('click', function () {
document.getElementById('photos_paris_brest_1_agrandi')
photos_paris_brest_1_agrandi.style.visibility = 'visible';
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'visible';
})

photos_paris_brest_1_miniature.addEventListener('click', function () {
document.getElementById('arrierePlan')
photos_paris_brest_1_agrandi.style.visibility = 'visible';
})

arrierePlan.addEventListener('click', function () {
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'hidden';
photos_paris_brest_1_agrandi.style.visibility = 'hidden';
})


