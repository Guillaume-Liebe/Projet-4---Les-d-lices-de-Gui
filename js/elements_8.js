   
/*Agrandissements images Barquette au marron*/
var photos_baba_au_rhum_1_miniature = document.getElementById('photos_baba_au_rhum_1')

photos_baba_au_rhum_1_miniature.addEventListener('click', function () {
document.getElementById('photos_baba_au_rhum_1_agrandi')
photos_baba_au_rhum_1_agrandi.style.visibility = 'visible';
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'visible';
})

photos_baba_au_rhum_1_miniature.addEventListener('click', function () {
document.getElementById('arrierePlan')
photos_baba_au_rhum_1_agrandi.style.visibility = 'visible';
})

arrierePlan.addEventListener('click', function () {
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'hidden';
photos_baba_au_rhum_1_agrandi.style.visibility = 'hidden';
})


