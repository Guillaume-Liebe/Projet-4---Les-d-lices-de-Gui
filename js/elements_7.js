   
/*Agrandissements images Barquette au marron*/
var photos_barquette_au_marron_1_miniature = document.getElementById('photos_barquette_au_marron_1')

photos_barquette_au_marron_1_miniature.addEventListener('click', function () {
document.getElementById('photos_barquette_au_marron_1_agrandi')
photos_barquette_au_marron_1_agrandi.style.visibility = 'visible';
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'visible';
})

photos_barquette_au_marron_1_miniature.addEventListener('click', function () {
document.getElementById('arrierePlan')
photos_barquette_au_marron_1_agrandi.style.visibility = 'visible';
})

arrierePlan.addEventListener('click', function () {
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'hidden';
photos_barquette_au_marron_1_agrandi.style.visibility = 'hidden';
})


