
    /*Agrandissements images fraisier*/
    var photos_fraisier_1_miniature = document.getElementById('photos_fraisier_1')
    photos_fraisier_1_miniature.addEventListener('click', function () {
      document.getElementById('photos_fraisier_1_agrandi')
      photos_fraisier_1_agrandi.style.visibility = 'visible';
      document.getElementById('arrierePlan')
      arrierePlan.style.visibility = 'visible';
    })

    photos_fraisier_1_miniature.addEventListener('click', function () {
      document.getElementById('arrierePlan')
      photos_fraisier_1_agrandi.style.visibility = 'visible';
    })

    arrierePlan.addEventListener('click', function () {
      document.getElementById('arrierePlan')
      arrierePlan.style.visibility = 'hidden';
      photos_fraisier_1_agrandi.style.visibility = 'hidden';
    })

