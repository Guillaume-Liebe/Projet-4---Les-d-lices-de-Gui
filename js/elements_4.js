   
/*Agrandissements images Pithiviers*/
var photos_pithiviers_1_miniature = document.getElementById('photos_pithiviers_1')

photos_pithiviers_1_miniature.addEventListener('click', function () {
document.getElementById('photos_pithiviers_1_agrandi')
photos_pithiviers_1_agrandi.style.visibility = 'visible';
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'visible';
})

photos_pithiviers_1_miniature.addEventListener('click', function () {
document.getElementById('arrierePlan')
photos_pithiviers_1_agrandi.style.visibility = 'visible';
})

arrierePlan.addEventListener('click', function () {
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'hidden';
photos_pithiviers_1_agrandi.style.visibility = 'hidden';
})


