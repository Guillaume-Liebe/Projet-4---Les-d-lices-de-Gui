   
/*Agrandissements images Eclair au chocolat*/
var photos_eclair_au_chocolat_1_miniature = document.getElementById('photos_eclair_au_chocolat_1')

photos_eclair_au_chocolat_1_miniature.addEventListener('click', function () {
document.getElementById('photos_eclair_au_chocolat_1_agrandi')
photos_eclair_au_chocolat_1_agrandi.style.visibility = 'visible';
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'visible';
})

photos_eclair_au_chocolat_1_miniature.addEventListener('click', function () {
document.getElementById('arrierePlan')
photos_eclair_au_chocolat_1_agrandi.style.visibility = 'visible';
})

arrierePlan.addEventListener('click', function () {
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'hidden';
photos_eclair_au_chocolat_1_agrandi.style.visibility = 'hidden';
})


