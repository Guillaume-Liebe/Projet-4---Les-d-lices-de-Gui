   var photos_tarte_aux_fruits_1_miniature = document.getElementById('photos_tarte_aux_fruits_1')

   photos_tarte_aux_fruits_1_miniature.addEventListener('click', function () {
   document.getElementById('photos_tarte_aux_fruits_1_agrandi')
   photos_tarte_aux_fruits_1_agrandi.style.visibility = 'visible';
   document.getElementById('arrierePlan')
   arrierePlan.style.visibility = 'visible';
 })

   photos_tarte_aux_fruits_1_miniature.addEventListener('click', function () {
   document.getElementById('arrierePlan')
   photos_tarte_aux_fruits_1_agrandi.style.visibility = 'visible';
 })

 arrierePlan.addEventListener('click', function () {
   document.getElementById('arrierePlan')
   arrierePlan.style.visibility = 'hidden';
   photos_tarte_aux_fruits_1_agrandi.style.visibility = 'hidden';
 })

