   
   /*Agrandissements images choux à la crème*/
   var photos_choux_a_la_creme_1_miniature = document.getElementById('photos_choux_a_la_creme_1')

   photos_choux_a_la_creme_1_miniature.addEventListener('click', function () {
   document.getElementById('photos_choux_a_la_creme_1_agrandi')
   photos_choux_a_la_creme_1_agrandi.style.visibility = 'visible';
   document.getElementById('arrierePlan')
   arrierePlan.style.visibility = 'visible';
   })

   photos_choux_a_la_creme_1_miniature.addEventListener('click', function () {
   document.getElementById('arrierePlan')
   photos_choux_a_la_creme_1_agrandi.style.visibility = 'visible';
   })

   arrierePlan.addEventListener('click', function () {
   document.getElementById('arrierePlan')
   arrierePlan.style.visibility = 'hidden';
   photos_choux_a_la_creme_1_agrandi.style.visibility = 'hidden';
   })


