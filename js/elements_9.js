   
/*Agrandissements images maracons*/
var photos_macarons_1_miniature = document.getElementById('photos_macarons_1')

photos_macarons_1_miniature.addEventListener('click', function () {
document.getElementById('photos_macarons_1_agrandi')
photos_macarons_1_agrandi.style.visibility = 'visible';
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'visible';
})

photos_macarons_1_miniature.addEventListener('click', function () {
document.getElementById('arrierePlan')
photos_macarons_1_agrandi.style.visibility = 'visible';
})

arrierePlan.addEventListener('click', function () {
document.getElementById('arrierePlan')
arrierePlan.style.visibility = 'hidden';
photos_macarons_1_agrandi.style.visibility = 'hidden';
})


